'''
Question Link: https://www.hackerrank.com/challenges/designer-door-mat/problem?isFullScreen=true

Task:
    Pattern printing
    NxM -> N is odd number and M = N x 3

    Size: 7 x 21 
    ---------.|.---------
    ------.|..|..|.------
    ---.|..|..|..|..|.---
    -------WELCOME-------
    ---.|..|..|..|..|.---
    ------.|..|..|.------
    ---------.|.---------
    
    Size: 11 x 33
    ---------------.|.---------------
    ------------.|..|..|.------------
    ---------.|..|..|..|..|.---------
    ------.|..|..|..|..|..|..|.------
    ---.|..|..|..|..|..|..|..|..|.---
    -------------WELCOME-------------
    ---.|..|..|..|..|..|..|..|..|.---
    ------.|..|..|..|..|..|..|.------
    ---------.|..|..|..|..|.---------
    ------------.|..|..|.------------
    ---------------.|.---------------
'''

if __name__ == '__main__':
    n,m = map(int, input().split())
    # for n, m = 7, 21
    # 7//2 = 3 -> '---'*3 both side
    # 7-3*2 = 7 - 6 = 1 -> '.|.'*1
    for i in range(n//2, 0, -1):
        print('---'*i+'.|.'*(n-i*2)+'---'*i)
    # length of 'WELCOME' -> 7. -> (21-7)//2 -> 14//2 -> 7 -> '-'*7 both side
    print('-'*((m-7)//2)+'WELCOME'+'-'*((m-7)//2))
    # lower pattern
    for i in range(1, n//2+1):
        print('---'*i+'.|.'*(n-i*2)+'---'*i)